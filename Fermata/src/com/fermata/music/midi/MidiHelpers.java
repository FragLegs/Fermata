package com.fermata.music.midi;

import com.fermata.ui.PianoKeyType;

public class MidiHelpers {

	// takes a midi value and returns its location on a piano keyboard
	public static PianoKeyType MidiToPianoKeyType(int midiValue) throws IllegalArgumentException
	{		
		switch (midiValue % 12)
		{
		case 1:
		case 3:
		case 6:
		case 8:
		case 10:
			return PianoKeyType.BLACK;
		case 2:
		case 7:
		case 9:
			return PianoKeyType.CENTER;
		case 0:
		case 5:
			return PianoKeyType.LEFT;
		case 4:
		case 11:
			return PianoKeyType.RIGHT;
		default:
			throw new IllegalArgumentException("Found unrecognized MIDI value!");
		}
	}
	
}

